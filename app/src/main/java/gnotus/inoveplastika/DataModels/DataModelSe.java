package gnotus.inoveplastika.DataModels;

public class DataModelSe {


    private String ref;
    private String lote;
    private Boolean inactivo;
    private String design;
    Double fconversao;
    private String unidade;
    private String uni2;


    public String getRef() {
        return ref;
    }

    public void setRef(String ref) {
        this.ref = ref;
    }

    public String getLote() {
        return lote;
    }

    public void setLote(String lote) {
        this.lote = lote;
    }

    public Boolean getInactivo() {
        return inactivo;
    }

    public void setInactivo(Boolean inactivo) {
        this.inactivo = inactivo;
    }

    public String getDesign() {
        return design;
    }

    public void setDesign(String design) {
        this.design = design;
    }

    public Double getFconversao() {
        return fconversao;
    }

    public void setFconversao(Double fconversao) {
        this.fconversao = fconversao;
    }

    public String getUnidade() {
        return unidade;
    }

    public void setUnidade(String unidade) {
        this.unidade = unidade;
    }

    public String getUni2() {
        return uni2;
    }

    public void setUni2(String uni2) {
        this.uni2 = uni2;
    }
}
